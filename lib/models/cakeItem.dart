class CakeItem {
  final String firebaseId;
  final String typeMain;
  final String id;
  final String imageUrl;
  final double weight;
  final String type;
  final String name;
  final String identifierString;
  final double price;
  CakeItem({
    required this.id,
    required this.firebaseId,
    required this.imageUrl,
    required this.typeMain,
    required this.weight,
    required this.type,
    required this.name,
    required this.identifierString,
    required this.price,
  });
}